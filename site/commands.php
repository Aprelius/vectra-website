    <p>The stats script contains:</p>
    <p><br />
    </p>
    !@stats RSN - Gives stats of given RSN<br />
    !@stats -p RSN   - Gives stats and % to 99 for the given RSN<br />
    !@stats -e RSN - Gives EXP for   all stats for the given RSN<br />
    !@stats -n RSN - Gives EXP to next level in ALL   skills at ones.<br />
    !@stats -r RSN - Gives RANK for all stats for the given   RSN<br />
    !@stats &gt;LVL RSN - Gives all stats OVER given level for RSN<br />
    !@stats   &lt;LVL RSN - Gives all stats UNDER given level for RSN<br />
    !@stats =LVL RSN -   Gives the ecact skills for the given RSN<br />
